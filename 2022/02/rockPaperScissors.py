Rock_Points = 1
Paper_Points = 2
Scissors_Points = 3

Lost_Points = 0
Draw_Points = 3
Won_Points = 6

def calculate_total_score(opponent_play_list, you_play_list):
    total_score = 0

    if len(opponent_play_list) == len(you_play_list):
        index = 0
        while index < len(you_play_list):
            round_score = 0

            # Score for the shape you selected:
            if you_play_list[index] == You_Play_Rock:
                round_score = round_score + Rock_Points
            if you_play_list[index] == You_Play_Paper:
                round_score = round_score + Paper_Points
            if you_play_list[index] == You_Play_Scissors:
                round_score = round_score + Scissors_Points

            # Score for the outcome of the round:
            if opponent_play_list[index] == Opponent_Rock:
                if you_play_list[index] == You_Play_Rock:
                    round_score = round_score + Draw_Points
                if you_play_list[index] == You_Play_Paper:
                    round_score = round_score + Won_Points
                if you_play_list[index] == You_Play_Scissors:
                    round_score = round_score + Lost_Points

            if opponent_play_list[index] == Opponent_Paper:
                if you_play_list[index] == You_Play_Rock:
                    round_score = round_score + Lost_Points
                if you_play_list[index] == You_Play_Paper:
                    round_score = round_score + Draw_Points
                if you_play_list[index] == You_Play_Scissors:
                    round_score = round_score + Won_Points

            if opponent_play_list[index] == Opponent_Scissors:
                if you_play_list[index] == You_Play_Rock:
                    round_score = round_score + Won_Points
                if you_play_list[index] == You_Play_Paper:
                    round_score = round_score + Lost_Points
                if you_play_list[index] == You_Play_Scissors:
                    round_score = round_score + Draw_Points

            total_score = total_score + round_score
            index = index + 1
    else:
        print("Error #1")

    return total_score

